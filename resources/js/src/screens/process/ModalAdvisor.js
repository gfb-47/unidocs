export default function ModalAdvisor({ professor, handleProfessorChange = () => { } }) {
  const classes = useStyles();
  const [order, setOrder] = React.useState('asc');
  const [orderBy, setOrderBy] = React.useState('calories');
  const [selected, setSelected] = React.useState(null);
  const [inputFilter, setInputFilter] = React.useState("");
  const [filteredList, setFilteredList] = React.useState([]);
  const [page, setPage] = React.useState(0);
  const [dense, setDense] = React.useState(false);
  const [rowsPerPage, setRowsPerPage] = React.useState(5);
  const [professors, setProfessors] = React.useState([]);
  const [, dispatch] = React.useContext(Context);


  const fetchProfessors = () => {
    dispatch(setLoading(true));
    api.getAllProfessors().then(res => {
      const result = res.data.data;
      setProfessors(result);
      setFilteredList(result)
      dispatch(setLoading(false));

    });
  };

  React.useEffect(() => {
    fetchProfessors();
    setSelected(professor)
  }, []);

  React.useEffect(() => {
    const filteredProfessor = professors.filter((p) => {
      return p.name.toLowerCase().includes(inputFilter.toLowerCase());
    });
    setFilteredList(filteredProfessor);
  }, [inputFilter])

  const handleClick = (event, name) => {

    setSelected(name);
  };

  const handleChangePage = (event, newPage) => {
    setPage(newPage);
  };

  const handleChangeRowsPerPage = (event) => {
    setRowsPerPage(parseInt(event.target.value, 10));
    setPage(0);
  };

  const handleFinish = () => {

    handleProfessorChange({ professor_id: selected })
  }

  const isSelected = (name) => selected === name;

  const emptyRows = rowsPerPage - Math.min(rowsPerPage, filteredList.length - page * rowsPerPage);

  return (
    <div className={classes.root}>
      <Paper className={classes.paper}>
        <Grid
          container
          direction="row"
          justify="space-between"
          alignItems="center"
        >
          <TextField
            id="standard-helperText"
            value={inputFilter}
            onChange={(e) => setInputFilter(e.target.value)}
            label="Nome do Professor"
            helperText="Filtre o professor pelo nome"
          />
          <Button variant="contained" onClick={handleFinish} color="primary">
            Finalizar Seleção
          </Button>
        </Grid>
        <TableContainer>
          <Table
            className={classes.table}
            aria-labelledby="tableTitle"
            size={dense ? 'small' : 'medium'}
            aria-label="enhanced table"
          >

            <TableBody>
              {stableSort(filteredList, getComparator(order, orderBy))
                .slice(page * rowsPerPage, page * rowsPerPage + rowsPerPage)
                .map((row, index) => {
                  const isItemSelected = isSelected(row.id);
                  const labelId = `enhanced-table-checkbox-${index}`;

                  return (
                    <TableRow
                      hover
                      onClick={(event) => handleClick(event, row.id)}
                      role="checkbox"
                      aria-checked={isItemSelected}

                      tabIndex={-2}
                      key={row.id}
                      selected={isItemSelected}
                    >
                      <TableCell padding="checkbox">
                        <Checkbox
                          checked={isItemSelected}
                          inputProps={{ 'aria-labelledby': labelId }}
                        />
                      </TableCell>
                      <TableCell component="th" id={labelId} align="left" scope="row" padding="none">
                        <div className={classes.userCell}>
                          <Avatar
                            style={{
                              marginRight: "1rem",
                              color: `${row.color}`,
                              backgroundColor: `${row.color}50`,
                            }}
                          >
                            {row.name[0]}
                          </Avatar>

                          <div>
                            <b>{row.name}</b> <br />
                          </div>
                        </div>
                      </TableCell>

                      <TableCell>{row.courses}</TableCell>
                      <TableCell
                        align="left"
                        className={row.active == 1 ? classes.itemActive : classes.itemInactive}
                      >
                        <span>{row.active == 1 ? 'ativo' : 'inativo'}</span>
                      </TableCell>
                    </TableRow>
                  );
                })}
              {emptyRows > 0 && (
                <TableRow style={{ height: (dense ? 33 : 53) * emptyRows }}>
                  <TableCell colSpan={6} />
                </TableRow>
              )}
            </TableBody>
          </Table>
        </TableContainer>
        <TablePagination
          rowsPerPageOptions={[5, 10, 25]}
          component="div"
          count={filteredList.length}
          rowsPerPage={rowsPerPage}
          page={page}
          onChangePage={handleChangePage}
          onChangeRowsPerPage={handleChangeRowsPerPage}
        />
      </Paper>
    </div>
  );
}
